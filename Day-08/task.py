# 1.Convert tuple to list and set
# Tuple 
x = (1, 2, 3, 4)
print("Tuple :", x)
print("Data type :", type(x))
print()

# Tuple to List
y = list(x)
print("Tuple to List : ", y)
print("Data Type : ", type(y))
print()

# Tuple to Set
z = set(x)
print("Tuple to Set : ", z)
print("Data Type : ", type(z))


# 2.convert set to list and tuple
# Set
a = (10, 20, 30, 20)
print("Set :",a)
print(type(a))
print()

# Tuple to List
b = list(a)
print("Tuple to List : ", b)
print("Data Type : ", type(b))
print()

# Tuple to Set
